//Dias Vividos

let anos,meses,dias,diasvividos

anos=parseInt(prompt("Digite Quantos Anos Você Possui: "));

meses=parseInt(prompt("Digite Quantidade de Meses vividos"));

dias=parseInt(prompt("Digite Quantidade de Dias do seu Ultimo MesVersario"));

diasvividos=anos*365+meses*30+dias

alert("A Quantidade de dias vividos É: "+diasvividos+" Dias ");
